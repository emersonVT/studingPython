# Declaring variables
name = "Emerson Tomo"
age = 25

#In Python, you can use the print function to print data to the terminal.
print(name)
print(age)

# Variables are case-sensitive
# If I use uppercase or lowercase in a variable with the same name, python can feel the letter and treat it  as different name.
Age = 30
AGE = 35

print(Age)
print(AGE)

# Using descriptive variable names
user_age = 25
user_name = "Emerson"

print(user_name)
print(user_age)

# Updating a variable
user_age = user_age + 1
print(user_age)
